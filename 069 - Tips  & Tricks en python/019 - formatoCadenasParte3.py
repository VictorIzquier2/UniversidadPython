# 3. Interpolacion

nombre = 'Juan'
error = 500
entero = 50
print(f'Hola, {nombre}')

# Este es el mismo ejemplo de hexadecimal con String interpolation
print(f'Error hexadecimal: {error:x}')

# Impresion de entero a flotante
print(f'Numero flotante: {entero:.2f}')

# Incluir expresiones o llamadas a funciones
a = 10
b = 3
print(f'Dividimos y damos formato: {a} / {b} = {a/b:.4f}')