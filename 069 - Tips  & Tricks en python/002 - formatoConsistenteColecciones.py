# Tip, Dar formato consistente a diccionarios, listas, o cualquier otra colección en python

# Definición de una lista en una sola línea
mi_lista = ['Juan', 'Laura', 'Guadalupe']

# Dar formato a nuestra lista
mi_lista = [
  'Juan',
  'Laura',
  'Guadalupe'
]

# Dar formato a nuestra lista pero con posibles errores
mi_lista = [
  'Juan',
  'Laura',
  'Guadalupe'
  'Pedro'
]

# Formato consistente de colecciones implica agregar una coma al final
mi_lista= [
  'Juan',
  'Laura',
  'Guadalupe',
  'Pedro',
]

print(mi_lista)