"""
Suma: +
Resta: -
Multiplicación: *
División: /
División entera: //
Exponente: **
Módulo: %
"""

operandoA : int = 3
operandoB : int = 2
suma = operandoA + operandoB
resta = operandoA - operandoB
multiplicacion = operandoA * operandoB
division = operandoA / operandoB

print(f'Resultado Suma: {operandoA} + {operandoB} = {suma}')
print(f'Resultado Resta: {operandoA} - {operandoB} = {resta}')
print(f'Resultado Multiplicación: {operandoA} x {operandoB} = {multiplicacion}')
print(f'Resultado División: {operandoA} / {operandoB} = {division}')